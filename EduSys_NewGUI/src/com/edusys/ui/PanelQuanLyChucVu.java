    public void focusGained() {
        if (txtTimChucVu.getText().equals("Tìm kiếm chức vụ")) {
            txtTimChucVu.setText("");
        }
        txtTimChucVu.setForeground(Color.black);
    }

    public void focusLost() {
        if (txtTimChucVu.getText().equals("")) {
            txtTimChucVu.setText("Tìm kiếm chức vụ");
            txtTimChucVu.setForeground(new Color(102, 102, 102));
        }
    }

    public void fillTable() {
        //DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0); // Xóa tất cả các hàng trên JTable
        try {
            List<ChucVu> list;
            if (txtTimChucVu.getText().equals("") || txtTimChucVu.getText().equals("Tìm kiếm chức vụ")) {
                list = dao.selectAll(); // Đọc dữ liệu từ CSDL
            } else {
                list = dao.selectByKeyword(txtTimChucVu.getText());
            }
            ChucVuDAO cvDAO = new ChucVuDAO();
            for (ChucVu nv : list) {
                Object[] row = {nv.getMaCV(), nv.getTenCV()};
                model.addRow(row); // Thêm một hàng vào JTable
            }
        } catch (Exception e) {
            throw new RuntimeException(e);
//JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu!");
        }
    }

