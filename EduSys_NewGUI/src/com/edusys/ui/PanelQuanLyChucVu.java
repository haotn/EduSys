    public void clearForm() {
        ChucVu nv = new ChucVu();
        this.setForm(nv);
        //if(txtHoTenNV.getText().equals("nullnull")){
        //txtHoTenNV.setText("");
        // }
        this.row = -1;
        this.updateStatus();
    }

    public void edit() {
        String macv = (String) tblChucVu.getValueAt(this.row, 0);
        ChucVu nv = dao.selectById(macv);
        this.setForm(nv);
        this.updateStatus();
    }

    public void insert() {
        ChucVu nv = getForm();
        try {
            dao.insert(nv);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "thêm mới thành công!");
        } catch (Exception e) {
            //System.out.println(e);
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

    public void update() {
        ChucVu nv = getForm();
        if (MsgBox.confirm(this, "Bạn có chắc muốn cập nhật nhân viên này không?")) {
            try {
                dao.update(nv);
                this.fillTable();
                clearForm();
                //MsgBox.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại!");
            }
        }

    }

    public boolean checkForm() {
        ChucVu nv = getForm();
        if (isUpdate == false) {
            if (txtMaCV.getText().equals("") || txtTenCV.getText().equals("") || phanQuyen.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập (chọn) đầy đủ thông tin trước khi nhấn nút thêm!", "Error", 1);
                return false;
            }
        }
        for (int i = 0; i < dao.selectAll().size(); i++) {
            if (isUpdate == true) {
            } else {
                if (txtMaCV.getText().equals(dao.selectAll().get(i).getMaCV())) {
                    JOptionPane.showMessageDialog(this, "Mã chức vụ không được phép trùng!", "Error", 1);
                    return false;
                }
            }
        }
        return true;
    }

    public void delete() {
        if (!Auth.isManager()) {
            JOptionPane.showMessageDialog(this, "Bạn không có quyền chức vụ viên!", "Error", 1);
        } else {
            if (txtMaCV.getText().equals("adminQT")) {
                JOptionPane.showMessageDialog(this, "Không thể xóa chức vụ admin!", "Error", 1);
            } else if (dao.selectHaveEmployee(txtMaCV.getText()).size() > 0) {
                if (MsgBox.confirm(this, "Đã có nhân viên trong chức vụ này, bạn có chắc chắn muốn xóa chức vụ không?")) {
                    try {
                        dao.delete(txtMaCV.getText());
                        this.fillTable();
                        this.clearForm();
                        //MsgBox.alert(this, "Xóa thành công!");
                    } catch (Exception e) {
                        MsgBox.alert(this, "Xóa thất bại!");
                    }
                }
            } else if (MsgBox.confirm(this, "Bạn thực sự muốn xóa nhân viên này?")) {
                try {
                    dao.delete(txtMaCV.getText());
                    this.fillTable();
                    this.clearForm();
                    //MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                }
            }
        }
    }

    public void first() {
        this.row = 0;
        tblChucVu.setRowSelectionInterval(row, row);
        this.edit();
    }

    public void prev() {
        if (this.row > 0) {
            this.row--;
            tblChucVu.setRowSelectionInterval(row, row);
            this.edit();
        }
    }

    public void next() {
        if (this.row < tblChucVu.getRowCount() - 1) {
            this.row++;
            tblChucVu.setRowSelectionInterval(row, row);
            this.edit();
        }
    }

    public void last() {
        this.row = tblChucVu.getRowCount() - 1;
        tblChucVu.setRowSelectionInterval(row, row);
        this.edit();
    }

    public void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblChucVu.getRowCount() - 1);
        // Trạng thái Form
        txtMaCV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnCapNhat.setEnabled(edit);
        btnXoa.setEnabled(edit);
        // Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);

    }
