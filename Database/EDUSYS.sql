CREATE TABLE NHANVIEN(
	MANV NVARCHAR(50) PRIMARY KEY,
	MATKHAU NVARCHAR(50) NOT NULL,
	HO NVARCHAR(30) NOT NULL,
	TEN NVARCHAR(20) NOT NULL,
	VAITRO BIT DEFAULT 0
